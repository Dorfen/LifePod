int my_is_prime(int nb)
{
    int i;
    int j;

    i = 0;
    j = 0;
    if (nb < 1)
        return (0);
    while (i != nb){
        if (nb / 1)
            j = j + 1;
    }
    if (j == 2)
        return (1);
    else
        return (0);
}
